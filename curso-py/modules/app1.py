# A module call files, a package call folders
# Using "*" after "import", we can import all the actions of our archive of a module


# There are 3 ways to call file actions within a package:

# # First:
# from users.actions import save
# save()

# # Second:
# import users.actions
# users.actions.save()

# # Third:
# from users import actions
# actions.save()
